class else_if_lader{
    public static void main(String args[]){
        int a=100;
        if (a<50){
            System.out.println(a);
        }
        else if (a>101){
            System.out.println(a);
        }
        else if (a>72){
            System.out.println(a);
        }
        else{
            System.out.println(a);
        }
    }
}